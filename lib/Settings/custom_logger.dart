import 'package:logger/logger.dart';


class CustomLogger{


  /// //////////////////////////////////////////////////////////////////////////
  /// logger 객체 생성(StackTrace 있음)
  /// //////////////////////////////////////////////////////////////////////////
  Logger logger = Logger(
    printer: PrettyPrinter(
      colors: true,
      printEmojis: true,
    ),
  );


  /// //////////////////////////////////////////////////////////////////////////
  /// logger 객체 생성(StackTrace 없음)
  /// //////////////////////////////////////////////////////////////////////////
  Logger loggerNoStack = Logger(
    printer: PrettyPrinter(
      methodCount: 0,
      colors: true,
      printEmojis: true,
    ),
  );


  /// //////////////////////////////////////////////////////////////////////////
  /// debug 출력
  /// //////////////////////////////////////////////////////////////////////////
  void printD(String msg) {
    loggerNoStack.d(msg);
  }


  /// //////////////////////////////////////////////////////////////////////////
  /// info 출력
  /// //////////////////////////////////////////////////////////////////////////
  void printI(String msg) {
    loggerNoStack.i(msg);
  }


  /// //////////////////////////////////////////////////////////////////////////
  /// warning 출력
  /// //////////////////////////////////////////////////////////////////////////
  void printW(String msg) {
    logger.w(msg);
  }


  /// //////////////////////////////////////////////////////////////////////////
  /// error 출력
  /// //////////////////////////////////////////////////////////////////////////
  void printE(String msg) {
    logger.e(msg);
  }

}